                <p>SAKUTOHは、作品投稿型Webサービスです。ユーザー登録していただくと、自分の作品を自由に投稿することができます。また、タイムライン形式で作品を共有しているので、他ユーザーの作品を観覧することも可能です。</p>
                <p>なぜこのサービスを作ったのか。それは、ライターと雇用主との間に対等な関係性を構築するためです。現状、ライターは雇用主の依頼を受け、記事を寄稿している場合がほとんどです。しかしこの場合、ライターと雇用主との間に上下関係が生じてしまい、結果、ライターの労働環境の悪化（低賃金、長時間労働）といった問題につながってしまいます。</p>
                <p>この問題を解決するためには、ライターが雇用主に直接交渉できるような立場を作り上げることが必要です。そのための手段の1つとして、作品投稿型プラットフォームがあるのではないかと考えました。ライター側から作品を発信する。それを雇用主が観覧して、ライターをスカウトするような仕組みを作る。こうすれば、ライターの力量も把握しやすいですし、何よりライターと雇用主とが対等な立場に立つことができます。</p>
                <p>今後、サイトをさらに発展させ、プラットフォームとしての立ち位置、ライターと雇用主とをつなぐWebサービスとしての価値をさらに向上させたいと思っています。</p>
                <div class="about-title">
                    <h3>SAKUTOHの使い方</h3>
                </div>
